def soma(a, b, c):
    """
    Função que recebe três argumentos e retorna a soma
    :param a: Primeiro argumento
    :param b: Segundo argumento
    :param c: Terceiro argumento
    :return: Retorna a soma dos três argumentos
    """

    return a + b + c
