# Entrada de dados
print("""
==========================================
   Conversor de metros para centímetros
==========================================
""")

metros = float(input("Quantidade de metros: "))

# Processamento de dados
centimetros = metros * 100

# Saída de dados
print("""
==========================================
               Resultado
==========================================
""")

print(metros, "metros  = ", centimetros, "cm")
